// Loading animation for real images
function loadImages() {
    const images = document.querySelectorAll('.gallery-image, .hero-bg-image, .attraction-img');
    images.forEach((image, index) => {
        // Add loading animation for real images
        image.style.opacity = '0';
        image.style.transform = 'scale(1.1)';
        
        image.addEventListener('load', () => {
            setTimeout(() => {
                image.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
                image.style.opacity = '1';
                image.style.transform = 'scale(1)';
            }, index * 200);
        });
        
        // Handle images that might already be loaded
        if (image.complete) {
            setTimeout(() => {
                image.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
                image.style.opacity = '1';
                image.style.transform = 'scale(1)';
            }, index * 200);
        }
    });
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    loadImages();
    
    // Add loading animation to sections
    const sections = document.querySelectorAll('.section');
    sections.forEach((section, index) => {
        section.style.animationDelay = `${index * 0.2}s`;
    });
});

